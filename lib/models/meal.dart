//el package foundation.dart nos aporta (entre otras cosas) el decorator @required
import 'package:flutter/foundation.dart';

// Los enums nos permiten tener una variable que puede tomar diferentes valores
// para dart son numeros (0,1,2) pero para nosotros tenemos el nombre, y es mas fácil asociarlo
// dart no nos dejará asociar ningún valor que no sea uno de los establecidos.
enum Complexity {
  Simple,
  Challenging,
  Hard,
}

enum Affordability {
  Affordable,
  Pricey,
  Luxurious,
}

class Meal {
  final String id;
  final List<String> categories;
  final String title;
  final String imageUrl;
  final List<String> ingredients;
  final List<String> steps;
  final int duration;
  final Complexity complexity;
  final Affordability affordability;
  final bool isGlutenFree;
  final bool isLactoseFree;
  final bool isVegan;
  final bool isVegetarian;

  const Meal({
    @required this.id,
    @required this.categories,
    @required this.title,
    @required this.imageUrl,
    @required this.ingredients,
    @required this.steps,
    @required this.duration,
    @required this.complexity,
    @required this.affordability,
    @required this.isGlutenFree,
    @required this.isLactoseFree,
    @required this.isVegan,
    @required this.isVegetarian,
  });
}
